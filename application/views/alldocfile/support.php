        <ol class="text-gray-700 text-sm sm:text-base mb-4 list-decimal pl-5 space-y-2">
            <li>Penultimate version is defined as the most-recent-but-one major version of the listed browser.
                <ul class="text-gray-700 text-sm sm:text-base list-disc pl-5 mt-1 space-y-1">
                    <li>a. And major version is defined as a change to the whole number portion of a browser’s version number.</li>
                </ul>
                <ul class="text-gray-700 text-sm sm:text-base list-disc pl-5 mt-1 space-y-1">
                    <li>i. For example Google Chrome 110 is the major version release that supercedes Google Chrome 109. So long as Google Chrome 110 is the current version, Google Chrome 109 will be the penultimate version.</li>
                </ul>
            </li>
            <li>The 60 days of support for penultimate versions begins as of the release of the current version. Once a current browser release is more than 60 days old, EditfloMCE is no longer supported on the penultimate version.</li>
            <li>EditfloMCE is supported on the most recent update of the browsers listed above, on the operating systems listed above.</li>
            <li>EditfloMCE is not supported on browsers running in Quirks or backward compatibility modes.</li>
            <li>EditfloMCE is not supported on pre-release, beta, or other ‘technology preview’ browser releases.</li>
            <li>On browsers that offer a longer-term stable version (ie Firefox Extended Support Release and Chrome Extended stable), EditfloMCE is only supported on the current longer-term stable release.</li>
