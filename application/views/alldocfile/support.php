        <h2 class="text-xl font-semibold text-gray-800 mb-4">Notes on the supported web browsers table.</h2>
        <ol class="text-gray-700 text-base mb-4 list-decimal pl-5 space-y-2">
            <li>Penultimate version is defined as the most-recent-but-one major version of the listed browser.</li>
            <ul class="text-gray-700 text-base mb-4 pl-5 space-y-2">
                <li>a. And major version is defined as a change to the whole number portion of a browser’s version number.</li>
                <ul class="text-gray-700 text-base mb-4 pl-5 space-y-2">
                    <li>i. For example Google Chrome 110 is the major version release that supercedes Google Chrome 109. So long as Google Chrome 110 is the current version, Google Chrome 109 will be the penultimate version.</li>
                </ul>
            </ul>
            <li>The 60 days of support for penultimate versions begins as of the release of the current version. Once a current browser release is more than 60 days old, Editflo is no longer supported on the penultimate version.</li>
            <li>Editflo is supported on the most recent update of the browsers listed above, on the operating systems listed above.</li>
            <li>Editflo is not supported on browsers running in Quirks or backward compatibility modes.</li>
            <li>Editflo is not supported on pre-release, beta, or other ‘technology preview’ browser releases.</li>
            <li>On browsers that offer a longer-term stable version (ie Firefox Extended Support Release and Chrome Extended stable), Editflo is only supported on the current longer-term stable release.</li>
        </ol>
